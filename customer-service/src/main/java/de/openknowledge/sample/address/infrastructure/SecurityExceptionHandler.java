package de.openknowledge.sample.address.infrastructure;

import javax.enterprise.context.ApplicationScoped;
import javax.ws.rs.core.Response;
import javax.ws.rs.ext.ExceptionMapper;
import javax.ws.rs.ext.Provider;

@Provider
@ApplicationScoped
public class SecurityExceptionHandler implements ExceptionMapper<SecurityException> {

    public Response toResponse(SecurityException exception) {
        return Integer.toString(Response.Status.FORBIDDEN.getStatusCode()).equals(exception.getMessage())
            ? Response.status(Response.Status.FORBIDDEN).build()
            : Response.status(Response.Status.UNAUTHORIZED).build();
    }
}
